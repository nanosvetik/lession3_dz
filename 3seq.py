# Получаем ввод от пользователя для первого списка
input_list1 = input("Введите элементы 1-го списка через запятую: ")

# Разделяем строку на элементы и преобразуем их в множество целых чисел
set1 = set(int(x) for x in input_list1.split(','))

# Получаем ввод от пользователя для второго списка
input_list2 = input("Введите элементы 2-го списка через запятую: ")

# Разделяем строку на элементы и преобразуем их в множество целых чисел
set2 = set(int(x) for x in input_list2.split(','))

# Выполняем разность множеств (удаляем из первого списка элементы, которые есть во втором)
result_set = set1 - set2

# Преобразуем результат обратно в список для вывода
result_list = list(result_set)

# Выводим результат
print("Результат:", result_list)